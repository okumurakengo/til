public class MyApp03Calc {
    public static void main(String[] args) {
        int i;
        i = 10 / 3;
        System.out.println(i);
        i = 10 % 3;
        System.out.println(i);
        int x = 5;
        x++;
        System.out.println(x);
        x--;
        System.out.println(x);

        x = 5;
        x += 12;
        System.out.println(x);

        String s;
        s = "hello " + "world";
        System.out.println(s);
    }
}
